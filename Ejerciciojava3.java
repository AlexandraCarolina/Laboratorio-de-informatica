package Ejerciciojava3;
public class java {
    public static void main(String[] args){
        Scanner sc = new Scanner(System.in);
        int n;
        System.out.print("Introduce un numero: ");
        n = sc.nextInt();
        System.out.println("Numero 1 hasta el numero ingresado");
        for (n = 1; n <= n; n++){
            System.out.println(n); 
        }
    }
    
}

    
    

